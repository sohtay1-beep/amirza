  4: ["مادر","گربه","درخت","کتاب","دفتر","لباس","معلم","دانش","خانه","اتاق","حیاط","کشور","جهان","دنیا","زمین","ماهی","خروس","الاغ","عقاب","کلاغ","جنگل","صحرا","کویر","دریا","قایق","چراغ","پرده","قاشق","ساعت","عینک","دکمه","سوزن","نخود","گوجه","پیاز","خیار","هویج","کاهو","انار","کوزه","آینه","پنیر","برنج","بزرگ","کوچک","خسته","بهار","پلنگ"],
